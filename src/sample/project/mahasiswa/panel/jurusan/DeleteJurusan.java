    private void jButtonDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonDeleteActionPerformed
        // TODO add your handling code here
        int index = jTable1.getSelectedRow();
        if (index == -1) {
            JOptionPane.showMessageDialog(jButtonClear, "Silahkah seleksi baris yang akan dihapus!");
        } else {
            if (JOptionPane.showConfirmDialog(jButtonClear, "Anda yakin akan menghapus data ini", "Delete", JOptionPane.OK_CANCEL_OPTION) == 0) {
                int nim = Integer.parseInt(jTextFieldNim.getText());
                service.delete(nim);
                loadTable();
                clear();
            }
        }
    }//GEN-LAST:event_jButtonDeleteActionPerformed

