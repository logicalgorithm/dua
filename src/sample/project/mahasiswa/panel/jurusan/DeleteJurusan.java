    public final void loadTable() {
        service = new JurusanService();
        list = service.selectAll();
        tableModel = new TableModelJurusan(list);
        jTable1.setModel(tableModel);
    }

    public final void selectedData() {
        jTable1.getSelectionModel().addListSelectionListener(new ListSelectionListener() {

            @Override
            public void valueChanged(ListSelectionEvent e) {
                int index = jTable1.getSelectedRow();
                if (index != -1) {
                    int nimJurusan = (int) tableModel.getValueAt(index, 0);
                    String nim = String.valueOf(nimJurusan);
                    String jurusan = (String) tableModel.getValueAt(index, 1);

                    jTextFieldNim.setText(nim);
                    jTextFieldJurusan.setText(jurusan);
                    model.setSelectedItem(jurusan);
                }
            }
        });
    }

    public void clear() {
        jTextFieldNim.setText("");
        jTextFieldJurusan.setText("");
        jComboBoxJurusan.setSelectedItem(null);
    }

